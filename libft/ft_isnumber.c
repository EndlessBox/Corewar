#include "libft.h"

/*
**	--------------------------------------------------------------------------
**	ft_isnumber is for checking the string if it is a number				  |
**	--------------------------------------------------------------------------
*/

int		ft_isnumber(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (-1);
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] && ft_isdigit(s[i]))
		i++;
	return (s[i] == '\0');
}
