#include "libft.h"

void			*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		*(char*)dst = *(char*)src;
		i++;
		src++;
		dst++;
	}
	return (dst - n);
}
